    private void Limpiar(){
        tf2.setText("");
        tf3.setText("");
        tf4.setText("");
        tf5.setText("");
        tf6.setText("");
        tf2.grabFocus();
    }
    
    private void Llenar(TLibro Lib){
        Lib.setISBN(tf2.getText());
        Lib.setTitulo(tf3.getText());
        Lib.setAutor(tf4.getText());
        Lib.setPaginas(Integer.parseInt(tf5.getText()));
        Lib.setPrecio(Float.parseFloat(tf6.getText()));
    }
    
    private void Mostrar(int Fila,TLibro Lib){
        tab.setValueAt(Lib.getISBN(),Fila,0);
        tab.setValueAt(Lib.getTitulo(),Fila,1);
        tab.setValueAt(Lib.getAutor(),Fila,2);
        tab.setValueAt(Lib.getPagina(),Fila,3);
         tab.setValueAt(Lib.getPrecio(),Fila,4);
    }
    
    private void Mostrar(){
        int i;
        for(i = 0; i < VL.getTam();i++){
            Mostrar(i,VL.getVec(i));
        }
    }

