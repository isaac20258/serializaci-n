    private void B1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B1ActionPerformed
        // TODO add your handling code here:
        int N;
        N = Integer.parseInt(tf1.getText());
        VL.setTam(N);
        ModT.setRowCount(N);
        sp1.setValue(0);
        Mods.setMaximum(N-1);
        Limpiar();
        B2.setEnabled(N > 0);
        B3.setEnabled(N > 0);
    }//GEN-LAST:event_B1ActionPerformed

    private void B2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B2ActionPerformed
        // TODO add your handling code here:
        int Pos;
        TLibro Lib;
        Pos = (int)sp1.getValue();
        Lib = VL.getVec(Pos);
        Llenar(Lib);
        Mostrar(Pos, Lib);
        Pos++;
        if(Pos < VL.getTam()){
            sp1.setValue(Pos);
        }
        Limpiar();
    }//GEN-LAST:event_B2ActionPerformed

    private void B3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B3ActionPerformed
        // TODO add your handling code here:
        Dlg.setDialogTitle("Guardar Libro como");
        if(Dlg.showSaveDialog(null)==JFileChooser.APPROVE_OPTION){
            VL.Guardar(Dlg.getSelectedFile().getAbsolutePath());
            javax.swing.JOptionPane.showMessageDialog(null,"Libros guardados");
        }
    }//GEN-LAST:event_B3ActionPerformed

    private void B4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B4ActionPerformed
        // TODO add your handling code here:
        Dlg.setDialogTitle("Cargar archivo de libros");
        if(Dlg.showOpenDialog(null)==JFileChooser.APPROVE_OPTION){
            VL.Cargar(Dlg.getSelectedFile().getAbsolutePath());
            sp1.setValue(0);
            ModT.setRowCount(VL.getTam());
            Mods.setMaximum(VL.getTam()-1);
            tf1.setText(VL.getTam()+ "");
            Limpiar();
            B2.setEnabled(VL.getTam() > 0);
            B3.setEnabled(VL.getTam() > 0);
            Mostrar();
        }
    }//GEN-LAST:event_B4ActionPerformed

